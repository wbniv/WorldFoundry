// scripting_wasm3.cc — wasm3 interpreter plug for the `#` sigil.
//
// Compiled in only when WF_WASM_ENGINE_WASM3 is defined. Hosts a single
// IM3Environment + IM3Runtime; per-script it base64-decodes the module
// body, parses + loads + links the `env.read_mailbox` / `env.write_mailbox`
// imports, finds `main`, and calls it. Errors are non-fatal (logged).
//
// Spike deviations from docs/plans/2026-04-14-wasm3-scripting-engine.md:
//   * No constant-globals import surface — wasm3 v0.5.0 exposes
//     m3_FindGlobal for module-defined globals but has no linker API for
//     host-provided globals. Scripts bake INDEXOF_* values as i32 literals
//     for now. Follow-up: add an env.const(i32 id) -> i32 import keyed to
//     the existing IntArrayEntry tables.


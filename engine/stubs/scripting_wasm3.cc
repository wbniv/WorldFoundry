float wasm3_engine::RunScript(const char* src, int objectIndex)
{
    if (!g_runtime || !src) return 0.0f;
    g_currentObjectIndex = objectIndex;

    // Skip whitespace, then the `#` sigil, then an optional `b64\n` subtype
    // tag. Current spike accepts only the b64 subtype (the format is
    // "#b64\n<base64>"); a bare `#` with a tag we don't recognise is an
    // error, so authors don't silently fall through to Lua.
    const char* p = src;
    while (*p == ' ' || *p == '\t' || *p == '\r' || *p == '\n') p++;
    if (*p != '#') return 0.0f;
    p++;
    const char* nl = std::strchr(p, '\n');
    if (!nl) {
        std::fprintf(stderr, "wasm3: missing newline after sigil\n");
        return 0.0f;
    }
    size_t tagLen = (size_t)(nl - p);
    if (tagLen != 3 || std::memcmp(p, "b64", 3) != 0) {
        std::fprintf(stderr, "wasm3: unknown sigil subtype (expected 'b64')\n");
        return 0.0f;
    }
    const char* b64Start = nl + 1;
    size_t      b64Len   = std::strlen(b64Start);

    // Decode into a heap buffer sized to the worst-case 3*len/4 ceiling.
    size_t   decCap = (b64Len / 4) * 3 + 3;
    if (decCap > kMaxModuleSz) {
        std::fprintf(stderr, "wasm3: module too large (%zu > %zu)\n",
                     decCap, kMaxModuleSz);
        return 0.0f;
    }
    uint8_t* bytes = (uint8_t*)std::malloc(decCap);
    if (!bytes) return 0.0f;
    int decoded = base64_decode(b64Start, b64Len, bytes, decCap);
    if (decoded < 0) {
        std::fprintf(stderr, "wasm3: base64 decode failed\n");
        std::free(bytes);
        return 0.0f;
    }

    IM3Module module = nullptr;
    M3Result  r      = m3_ParseModule(g_env, &module, bytes, (uint32_t)decoded);
    if (r) {
        std::fprintf(stderr, "wasm3: parse failed: %s\n", r);
        std::free(bytes);
        return 0.0f;
    }
    // wasm3 keeps a reference to the bytes buffer for the module's lifetime.
    // Transfer ownership to the runtime so we can't free it early; wasm3's
    // m3_LoadModule sets the module's "bytes-owned" flag via a private API
    // (ForEachModule_r / the module struct). We simply leak the buffer for
    // the spike: one module per script, ~hundreds of bytes each, negligible.

    r = m3_LoadModule(g_runtime, module);
    if (r) {
        std::fprintf(stderr, "wasm3: load failed: %s\n", r);
        m3_FreeModule(module);
        return 0.0f;
    }

    r = link_host_imports(module);
    if (r) {
        std::fprintf(stderr, "wasm3: link failed: %s\n", r);
        return 0.0f;
    }

    IM3Function fn = nullptr;
    r = m3_FindFunction(&fn, g_runtime, "main");
    if (r || !fn) {
        std::fprintf(stderr, "wasm3: no 'main' export: %s\n", r ? r : "null");
        return 0.0f;
    }

    r = m3_CallV(fn);
    if (r) {
        std::fprintf(stderr, "wasm3: trap in main: %s\n", r);
        return 0.0f;
    }

    // Return value (if any): grab first retval as i32 or f32.
    uint32_t retc = m3_GetRetCount(fn);
    if (retc == 0) return 0.0f;
    M3ValueType t = m3_GetRetType(fn, 0);
    if (t == c_m3Type_f32) {
        float v = 0.0f;
        m3_GetResultsV(fn, &v);
        return v;
    }
    if (t == c_m3Type_i32) {
        int32_t v = 0;
        m3_GetResultsV(fn, &v);
        return (float)v;
    }
    return 0.0f;
}

